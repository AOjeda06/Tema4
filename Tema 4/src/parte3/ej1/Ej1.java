package parte3.ej1;

/**
 * Esta clase contiene métodos para sumar números enteros y decimales.
 */
public class Ej1 {

	/**
	 * Suma dos números enteros.
	 * 
	 * @param a Primer número entero a sumar.
	 * @param b Segundo número entero a sumar.
	 * @return La suma de los dos números enteros.
	 */
	public static int suma(int a, int b) {
		return (a + b);
	}

	/**
	 * Suma dos números decimales.
	 * 
	 * @param a Primer número decimal a sumar.
	 * @param b Segundo número decimal a sumar.
	 * @return La suma de los dos números decimales.
	 */
	public static double suma(double a, double b) {
		return (a + b);
	}
}
